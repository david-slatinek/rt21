uint8_t i2c1_pisiRegister(uint8_t naprava, uint8_t reg, uint8_t podatek)
{
  naprava <<= 1;
  return HAL_I2C_Mem_Write(&hi2c1, naprava, reg, I2C_MEMADD_SIZE_8BIT, &podatek, 1, 10);
}

void i2c1_beriRegistre(uint8_t naprava, uint8_t reg, uint8_t* podatek, uint8_t dolzina)
{
  if (dolzina > 1)  // ce je naprava 0x19 moramo postaviti ta bit, ce zelimo brati vec zlogov
    reg |= 0x80;
  naprava <<= 1;
  HAL_I2C_Mem_Read(&hi2c1, naprava, reg, I2C_MEMADD_SIZE_8BIT, podatek, dolzina, dolzina);
}

void initLSM303DLHC()
{
  // Initialize sensor
  i2c1_pisiRegister(0x19, 0x20, 0x47);  // Wake up accelerometer and enable all 3 axes, set to 50hz
  i2c1_pisiRegister(0x19, 0x23, 0x98);  // High resolution, update value only after read, +-4g
  i2c1_pisiRegister(0x19, 0x25, 0x40);  // Enable INT1 interrupt
  i2c1_pisiRegister(0x19, 0x22, 0x10);	// DRDY on INT1

  // Read all registers to wake up interrupt
  uint16_t tmp;
  for (uint8_t reg = 0x07; reg <= 0x3F; reg++) {
	  i2c1_beriRegistre(0x19, reg, (uint8_t*)&tmp, 2);
  }
}


void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
  // Check if the interrupt was called from the correct pin
  if (GPIO_Pin == GPIO_PIN_4) {

	  // DEBUG - are interrupts being triggered? flash led
	  x++;
	  if (x == 1) HAL_GPIO_TogglePin(GPIOE, GPIO_PIN_10);
	  if (x > 50) x = 0;


	  // Reset measurement values
	  measurement[0] = 0xAAAB;
	  measurement[1] = count;
	  count++;
	  measurement[2] = 0;
	  measurement[3] = 0,
	  measurement[4] = 0;

	  // Read x,y,z and temperature from sensor
	  i2c1_beriRegistre(0x19, 0x28, (uint8_t*)&measurement[2], 6);

	  // Set flag for while loop write
	  new_values = 1;
  }
}
